    private void cbxModeloItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbxModeloItemStateChanged
        // TODO add your handling code here:
        if(evt.getStateChange() == 1){
            int position = this.cbxModelo.getSelectedIndex();
            Modelo modelo = this.ModeloList.get(position);
            this.tbxCategoria.setText(modelo.getTipo().getCategoria().getNombre());
            this.tbxTipo.setText(modelo.getTipo().getNombre());
            this.tbxMarca.setText(modelo.getMarca().getNombre());
        } else {
            this.tbxCategoria.setText("");
            this.tbxTipo.setText("");
            this.tbxMarca.setText("");
        }
    }//GEN-LAST:event_cbxModeloItemStateChanged

