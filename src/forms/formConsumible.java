    public void loadModelo(List<Modelo> list) {
        this.ModeloList = new ArrayList<>();
        this.cbxModelo.removeAllItems();
        list.forEach((data) -> {
            if(data.getTipo().getClase()){
                this.ModeloList.add(data);
                this.cbxModelo.addItem(data.getTipo().getNombre() + " - " + data.getNombre() + " (" + data.getMarca().getNombre() + ")");
            }
        });
    }
    
