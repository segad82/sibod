    @Override
    public Consumible getModel() {
        if(this.model == null)
            this.model = new Consumible(0);
        this.model.setModelo(this.ModeloList.get(this.cbxModelo.getSelectedIndex()));
        this.model.setCantidad(Integer.parseInt(this.tbxCantidad.getText()));
        this.model.setCantidad_minima(Integer.parseInt(this.tbxCantidadMinima.getText()));
        this.model.setEstado(this.chbxEstado.isSelected());
        return this.model;
    }

    @Override
    public void setModel(Consumible model) {
        this.model = model;
        this.tbxCategoria.setText(this.model != null ? this.model.getModelo().getTipo().getCategoria().getNombre() : "");
        this.tbxTipo.setText(this.model != null ? this.model.getModelo().getTipo().getNombre(): "");
        this.tbxMarca.setText(this.model != null ? this.model.getModelo().getMarca().getNombre() : "");
        this.cbxModelo.setSelectedItem(this.ModeloList != null && this.model != null ? this.model.getModelo().getNombre() : "");
        this.tbxCantidad.setText(this.model != null ? Integer.toString(this.model.getCantidad()) : "");
        this.tbxCantidadMinima.setText(this.model != null ? Integer.toString(this.model.getCantidad_minima()) : "");
        this.chbxEstado.setSelected(this.model != null ? this.model.getEstado(): false);
    }

    @Override
    public boolean checkView() {
        if(this.cbxModelo.getSelectedItem().toString().equals(""))
            this.addError("modelo", "El modelo es obligatorio.");
        if(!this.tbxCantidad.getText().equals("") && Integer.parseInt(this.tbxCantidad.getText()) < 0)
            this.addError("cantidad", "La cantidad actual debe ser mayor o igual a cero.");
        if(this.tbxCantidadMinima.getText().equals(""))
            this.addError("cantidad_minima", "La cantidad mínima es obligatoria.");
        else if(Integer.parseInt(this.tbxCantidadMinima.getText()) <= 0)
                this.addError("cantidad_minima", "La cantidad mínima debe ser mayor que cero.");
        return this.hasErrors();
    }

    @Override
    public boolean isNew() {
        return this.model == null || this.model.getId() == 0;
    }
    
